package com.java;

public class Ifelse {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;
        if(a+b < 10) {
            System.out.println("a + b is less than 10");
        } else {
            System.out.println("a + b is greater than 10");
        }
    }
}
